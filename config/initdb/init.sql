CREATE TABLE Color (
	rgba char(8),
	PRIMARY KEY(rgba)
);
CREATE TABLE Secret (
	id int AUTO_INCREMENT,
	nb_colors int NOT NULL,
	PRIMARY KEY(id)
);
CREATE TABLE Code (
	secret int NOT NULL,
	color char(8) NOT NULL,
	position int NOT NULL,
	PRIMARY KEY (secret, position),
	FOREIGN KEY(color) REFERENCES Color(rgba),
	FOREIGN secret REFERENCES Secret(secret),
);
CREATE TABLE Stat (
	id int AUTO_INCREMENT,
	secret int NOT NULL,
	record int NOT NULL,
	email varchar(255),
	PRIMARY KEY (id),
	FOREIGN KEY(secret) REFERENCES Secret(id)
);

